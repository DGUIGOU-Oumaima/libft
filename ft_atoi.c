#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	res;
	int	sign;
	int	i;

	i = 0;
	sign = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		res = res * 10 + str[i++] - 48;
	return (res * sign);
}

#include <stdio.h>
#include <stdlib.h>

int main()
{
	char *t = "2147483649";
	printf("%d \n", ft_atoi(t));
	printf("%d \n", atoi(t));
}
