#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)dst;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
}
